print("MENGHITUNG LUAS TRAPESIUM")
print("=========================")

tinggi = float(input("Masukkan Tinggi Trapesium : "))
panjangA = float(input("Masukkan Panjang Sisi A : "))
panjangB = float(input("Masukkan Panjang Sisi B : "))

luas = (tinggi * (panjangA + panjangB))/2

print("Luas Trapesium Tersebut adalah : {}".format(luas))