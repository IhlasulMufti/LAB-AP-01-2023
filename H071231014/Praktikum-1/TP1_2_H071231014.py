celcius = int(input('masukan suhu ='))

kelvin = int(celcius + 273)

reamur = int(4/5*celcius)

fahrenheit = int((9/5)*celcius + 32)

print('hasil konversi {} celcius ke kelvin = {} K'.format(celcius, kelvin)) 
print(f'hasil konversi {celcius} celcius ke reamur = {reamur}R')
print('hasil konversi {} celsius ke fahrenheit = {}F'.format(celcius, fahrenheit))
